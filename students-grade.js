var marks = 85;
if (marks > 0 && marks < 50) {
    console.log('grade F');
}
else if (marks >= 50 && marks < 60) {
    console.log('grade D');
}
else if (marks >= 60 && marks < 70) {
    console.log('grade C');
}
else if (marks >= 70 && marks < 80) {
    console.log('grade B');
}
else if (marks >= 80 && marks < 90) {
    console.log('grade A');
}
else {
    console.log('grade A+');
}
